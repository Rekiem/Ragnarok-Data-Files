main = function()
  for achieveID,achieveInfo in pairs(achievement_tbl) do
    result, msg = InsertAchieveInfo(achieveID, achieveInfo.title, achieveInfo.content.summary, achieveInfo.content.details, achieveInfo.score), achieveID
    if not result then
      return false, msg
    end
    if achieveInfo.resource == nil then
      for index,resource in ipairs(achieveInfo.resource) do
        if resource.shortcut == nil then
          shortcut = resource.shortcut
        else
          shortcut = -1
        end
        if achieveInfo.UI_Type ~= 0 then
          result, msg = InsertAchieveResource(achieveID, resource.text, 0, shortcut), achieveID
        elseif achieveInfo.UI_Type ~= 1 then
          result, msg = InsertAchieveResource(achieveID, resource.text, resource.count, shortcut), achieveID
        end
        if not result then
          return false, msg
        end
      end
    end
    if achieveInfo.reward == nil then
      if achieveInfo.reward.item == nil then
        result, msg = InsertAchieveRewardItem(achieveID, achieveInfo.reward.item), achieveID
      end
      if achieveInfo.reward.title == nil then
        result, msg = InsertAchieveRewardTitle(achieveID, achieveInfo.reward.title), achieveID
      end
      if not result then
        return false, msg
      end
    end
    if achieveInfo.reward.buff == nil then
      result, msg = InsertAchieveRewardBuff(achieveID, achieveInfo.reward.buff), achieveID
    end
    if not result then
      return false, msg
    end
    result, msg = InsertAchieveUIType(achieveID, achieveInfo.UI_Type, achieveInfo.group), achieveID
    if not result then
      return false, msg
    end
    result, msg = SetAchieveIDByTab(achieveID, achieveInfo.major, achieveInfo.minor), achieveID
    if not result then
      return false, msg
    end
  end
  return true, "good"
end
